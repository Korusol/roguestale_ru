import os, re, hashlib

def sha1(text):
    return hashlib.sha1(text.encode('utf-8')).hexdigest()

def update_checksum(xml):
    # Strip existing checksum from <dialogue ...> tag
    remove_cs = re.sub(r'(<dialogue\s[^>]*) checksum="[0-9a-f]+"', r'\1', xml, count=1)
    # Encode non-ASCII as &#N;
    encoded = []
    for c in remove_cs:
        cp = ord(c)
        if cp < 127:
            encoded.append(c)
        else:
            encoded.append(f'&#{cp};')
    cs = sha1(''.join(encoded))
    # Inject checksum after the tag name
    result = re.sub(r'(<dialogue\s)', rf'\1checksum="{cs}" ', remove_cs, count=1)
    return result

