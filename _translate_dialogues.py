# Process all dialogue files
os.makedirs('dialogues_Rus', exist_ok=True)
attr_pat = re.compile(r'(title|response)="([^"]*?)"')
counter = 0

for fname in sorted(os.listdir('dialogues')):
    if not fname.endswith('.xml'):
        continue
    path = os.path.join('dialogues', fname)
    text = open(path, 'r', encoding='utf-8').read()
    
    def translate_attr(m):
        attr_name = m.group(1)
        val = m.group(2)
        if not val or val.startswith('STR_BUY[') or re.match(r'^STR_[A-Z_0-9]+$', val) or re.match(r'^[a-z_][a-z_0-9]*$', val) or re.match(r'^[A-Z_0-9]+$', val) or re.match(r'^[\W\d_]+$', val):
            return m.group(0)
        if val in TR:
            return f'{attr_name}="{TR[val]}"'
        return m.group(0)
    
    translated = attr_pat.sub(translate_attr, text)
    translated = update_checksum(translated)
    out_path = os.path.join('dialogues_Rus', fname)
    open(out_path, 'w', encoding='utf-8').write(translated)
    counter += 1

print(f'Processed {counter} files to dialogues_Rus/')

# Check for any missed translations
missed = set()
attr_pat2 = re.compile(r'(title|response)="([^"]*?)"')
for fname in sorted(os.listdir('dialogues')):
    if not fname.endswith('.xml'):
        continue
    text = open(os.path.join('dialogues', fname), 'r', encoding='utf-8').read()
    for m in attr_pat2.finditer(text):
        val = m.group(2)
        if val and not val.startswith('STR_BUY[') and not re.match(r'^STR_[A-Z_0-9]+$', val) and not re.match(r'^[a-z_][a-z_0-9]*$', val) and not re.match(r'^[A-Z_0-9]+$', val) and not re.match(r'^[\W\d_]+$', val) and val not in TR:
            missed.add(val)

if missed:
    print(f'\nMissed translations ({len(missed)}):')
    for v in sorted(missed):
        print(f'  {v}')
else:
    print('All dialogue strings translated!')
